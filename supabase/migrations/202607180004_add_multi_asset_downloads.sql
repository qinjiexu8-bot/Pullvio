-- Keep the currently deployed single-artifact worker safe during the rolling
-- migration. Its expiry argument is intentionally ignored so all newly committed
-- artifacts receive the database-controlled 24-hour window.
create or replace function public.complete_media_job(
  p_job_id uuid,
  p_worker_id text,
  p_storage_bucket text,
  p_storage_path text,
  p_content_type text,
  p_content_disposition text,
  p_checksum_sha256 text,
  p_title text,
  p_thumbnail_url text,
  p_duration_seconds integer,
  p_file_size_bytes bigint,
  p_processing_seconds bigint,
  p_artifact_expires_at timestamptz
)
returns boolean
language plpgsql
security definer
set search_path = ''
as $$
declare
  v_kind text;
begin
  select media_kind into v_kind
  from public.download_jobs
  where id = p_job_id;
  if v_kind is null then return false; end if;

  return public.complete_media_job_v2(
    p_job_id,
    p_worker_id,
    jsonb_build_array(jsonb_build_object(
      'kind', v_kind,
      'storageBucket', p_storage_bucket,
      'storagePath', p_storage_path,
      'contentType', p_content_type,
      'contentDisposition', case
        when v_kind = 'video' then 'attachment; filename="pullvio-' || p_job_id::text || '-video.mp4"'
        else 'attachment; filename="pullvio-' || p_job_id::text || '-audio.mp3"'
      end,
      'checksumSha256', p_checksum_sha256,
      'fileSizeBytes', p_file_size_bytes
    )),
    p_title,
    p_thumbnail_url,
    p_duration_seconds,
    p_processing_seconds
  );
end;
$$;

