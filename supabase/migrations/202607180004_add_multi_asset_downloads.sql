alter table public.download_artifacts
  add column if not exists id bigint generated by default as identity,
  add column if not exists artifact_kind text,
  add column if not exists file_size_bytes bigint;

update public.download_artifacts as artifacts
set artifact_kind = case
      when artifacts.content_type like 'video/%' then 'video'
      when artifacts.content_type like 'audio/%' then 'audio'
      when artifacts.content_type like 'image/%' then 'thumbnail'
      else jobs.media_kind
    end,
    file_size_bytes = coalesce(artifacts.file_size_bytes, jobs.file_size_bytes, 0)
from public.download_jobs as jobs
where jobs.id = artifacts.job_id
  and (artifacts.artifact_kind is null or artifacts.file_size_bytes is null);

update public.download_artifacts
set expires_at = now() + interval '24 hours'
where expires_at is null;

alter table public.download_artifacts
  alter column artifact_kind set not null,
  alter column file_size_bytes set not null,
  alter column expires_at set not null,
  drop constraint if exists download_artifacts_pkey;

alter table public.download_artifacts
